<section>
    <h1>LES 4 GRANDS PRINCIPES DE L'ACCESSIBILITÉ NUMÉRIQUE</h1>
</section>

<div>
    <p>
        Le référentiel général d’accessibilité pour les administrations repose sur les 4 grands principes d’un site internet ou intranet accessible :

    </p>
</div>

<div>
    <h3>PRINCIPE 1 : UN SITE PERCEPTIBLE</h3>

    <ul>
        <li>Faire en sorte que chaque information soit perceptible par tout utilisateur, et par tous les sens (équivalents textuels pour tout contenu non textuel afin de permettre une synthèse vocale ou une transcription brailles, grands caractères, audio-description, langage simplifié…).</li>
        <li>Proposer des versions de remplacement aux médias audio, vidéo ou animations Flash.</li>
        <li>Créer un contenu présentable de différentes manières sans perte d’information (ex. : mise en page simplifiée).</li>
    </ul>

</div>

<div>
    <h3>PRINCIPE 2 : UN SITE UTILISABLE</h3>

    <ul>
        <li>Rendre toutes les fonctionnalités accessibles au clavier.</li>
        <li>Laisser à l’utilisateur suffisamment de temps pour lire et utiliser le contenu.</li>
        <li>Ne pas concevoir de contenu susceptible de provoquer des crises.</li>
        <li>Fournir à l’utilisateur des éléments d’orientation pour naviguer, trouver le contenu et se situer dans le site.</li>
    </ul>

</div>

<div>
    <h1>PRINCIPE 3 : UN SITE COMPRÉHENSIBLE</h1>

    <ul>
        <li>Rendre le contenu textuel lisible et compréhensible.</li>
        <li>Faire en sorte que les pages apparaissent et fonctionnent de manière prévisible.</li>
        <li>Aider l’utilisateur à éviter et à corriger les erreurs de saisie.</li>
    </ul>

</div>
<div>
    <h3>PRINCIPE 4 : UN SITE ROBUSTE</h3>

    <ul>
        <li>•Optimiser la compatibilité avec les agents utilisateurs actuels et futurs, y compris avec les technologies d’assistance.</li>
    </ul>
</div>